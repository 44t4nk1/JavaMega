class SwapThree {
    public static void main(String args[]){
        int a = 5, b=3, t;
        System.out.println("First number: "+a);
        System.out.println("First number: "+b);
        t=a;
        a=b;
        b=t;
        System.out.println("After Swapping...");
        System.out.println("First number: "+a);        
        System.out.println("First number: "+b);        
    }
}
